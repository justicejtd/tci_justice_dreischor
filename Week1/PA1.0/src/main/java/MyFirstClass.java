public class MyFirstClass {
    int age;
    String name;
    double weight;

    public MyFirstClass(int age, String name, double weight) {
        this.age = age;
        this.name = name;
        this.weight = weight;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public double getWeight() {
        return weight;
    }
}
